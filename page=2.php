          <h5>
พีธีกรรม เริ่มตั้งแต่วันแรม 1 ค่ำ เดือนสิบ ซึ่งถือว่าเป็นวันที่พญายมปล่อยตัวผู้ล่วงลับไปแล้วที่ (เรียกว่า "เปรต") มาจากนรก สำหรับวันนี้บางคนก็ประกอบพิธี บางคนจะประกอบพิธีใน
วันแรม 13 ค่ำ 14 ค่ำ และ 15 ค่ำ โดยการนำอาหารไปทำบุญที่วัดเรียกว่า "หมรับเล็ก" เป็นการต้อนรับบรรพบุรุษ และญาติมิตรที่ขึ้นมาจากนรกเท่านั้น 
                การเตรียมการสำหรับประเพณีสารทเดือนสิบ เริ่มขึ้นในวันแรม 13 ค่ำ เดือน 10 วันนี้ เรียกว่า "วันจ่าย" เป็นวันที่เตรียมหมรับ และจัดหมรับ คือการเตรียมสิ่งของต่าง ๆ ที่ใช้ในการจัดหมรับ 
เมื่อได้ของตามที่ต้องการแล้วก็เตรียมจัดหมรับ การจัดหมรับแต่เดิมใช้กระบุงเตี้ย ๆ ขนาดเล็กหรือใหญ่ก็ได้ แต่ภายหลังใช้ภาชนะได้หลายชนิด เช่น กระจาด ถาด กะละมัง ถัง หรือ กระเชอ สำหรับสิ่ง 
                การจัดหมรับ ชั้นแรกใส่ข้าวสารรองกระบุงแล้วใส่หอม กระเทียม พริก เกลือ กะปิ น้ำตาล และเครื่องปรุงอาคาว หวาน 
ที่เก็บไว้ได้นาน ๆ เช่น มะพร้าว ฟัก มัน กล้วย (ที่ยังไม่สุก) อ้อย ข้าวโพด ข่า ตะไคร้ ขมิ้น และพืชผักอื่น ๆ นอกจากนั้นก็ใส่ของใช้ในชีวิตประจำวัน เช่น น้ำมันมะพร้าว น้ำมันก๊าดไต้ ไม้ขีดไฟ หม้อ กะทะ 
ถ้วยชาม เข็ม ด้าย เครื่องเชี่ยนหมาก ได้แก่ หมาก พลู ปูน กานพลู การบูน พิมเสน สีเสียด ยาเส้น บุหรี่ ยาสามัญประจำบ้าน ธูปเทียน แล้วใส่สิ่งอันเป็นหัวใจอันสำคัญของหมรับคือ ขนม 5 อย่างมี ดังนี้ 
ขนมลา เป็นสัญลักษณ์แทนแพรพรรณเครื่องนุ่งห่ม 
                        ขนมพอง เป็นสัญลักษณ์แทนแพสำหรับบุรพชน ใช้ลอ่งข้ามห้วงมหรรณพ 
                       ขนมบ้า เป็นสัญลักษณ์แทนสะบ้า สำหรับบุรพชนจะได้ใช้เล่นสะบ้า ในวัน สงกรานต์ 
                       ขนมกง (ขนมไข่ปลา) เป็นสัญลักษณ์แทนเครื่องประดับ 
                       ขนมดีซำ เป็นสัญลักษณ์แทนเงินเบี้ย สำหรับใช้สอย          </h5>
