          <h5>
นครศรีธรรมราชตั้งอยู่ติดชายฝั่งทะเล ลักษณะภูมิอากาศเป็นแบบร้อนชื้น ในหน้าหนาวก็ไม่หนาวจัด เพียงแต่คนรู้สึกว่าอากาศเย็นลงกว่าปกติ เนื่องจากไม่เคยชินกับอากาศที่หนาวเย็นลง ตอนย่ำรุ่งเช้ามืด
จึงลุกขึ้นมาก่อไฟผิงเพื่อสร้างความอบอุ่น ประกอบกับชาวนครศรีธรรมราชยึดมั่นในพระพุทธศาสนา พุทธศาสนิกชนจึงพากันก่อกองไฟในวัดใกล้บ้าน แล้วนิมนต์พระภิกษุสงฆ์มาผิงไฟรับความอบอุ่นด้วย
ระยะเวลาดำเนินการการให้ทานไฟไม่มีกำหนดระยะเวลาที่แน่นอนตายตัวแล้วแต่ความสะดวกในการนัดหมาย แต่ส่วนใหญ่จะปฏิบัติในช่วงเดือนยี่ ซึ่งป็นช่วงที่มีอากาศหนาวเย็นที่สุด ชาวบ้านจะนัดหมาย
ไปพร้อมกันในเวลาย่ำรุ่งหรือตอนเช้ามืด ซึ่งจะเป็นวันไหนก็ได้
