          <h5>
ในสมัยที่พระเจ้าศรีธรรมโศกราชเป็นกษัตริย์ครองตามพรลิงค์(นครศรีธรรมราช) อยู่นั้น ได้มีการบูรณะปฏิสังขรณ์พระบรมธาตุเจดีย์ครั้งใหญ่และแล้วเสร็จในปี พ.ศ.๑๗๗๓ 
ขณะที่เตรียมสมโภชพระบรมธาตุอยู่นั้น ชาวปากพนังมากราบทูลว่า คลื่นได้ซัดเอาผ้าแถบยาวผืนหนึ่งซึ่งมีภาพเขียนเรื่องพุทธประวัติมาขึ้นที่ชายหาดปากพนัง ชาวปากพนังเก็บผ้านั้น
ถวายพระเจ้าศรีธรรมโศกราช พระองค์รับสั่งให้ซักผ้านั้นจนสะอาดเห็นภาพวาดพุทธประวัติ เรียกว่า “ผ้าพระบฏ” จึงรับสั่งให้ประกาศหาเจ้าของ ได้ความว่าชาวพุทธจากหงสากลุ่มหนึ่ง 
จะนำผ้าพระบฏไปบูชาพระพุทธบาทที่ลังกา แต่ถูกพายุพัดพามาขึ้นชายฝั่งปากพนัง เหลือผู้รอดชีวิตสิบคนพระเจ้าศรีธรรมโศกราชทรงมีความเห็นว่าควรนำผ้าพระบฏไปห่มพระบรมธาตุเจดีย์
เนื่องในโอกาสสมโภชพระบรมธาตุ แม้จะไม่ใช่พระพุทธบาทตามที่ตั้งใจ แต่ก็เป็นพระบรมสารีริกธาตุซึ่งเจ้าของผ้าพระบฏก็ยินดี การแห่ผ้าขึ้นธาตุจึงมีขึ้นตั้งแต่ปีนั้นและดำเนินการสืบต่อมา 
จนกลายเป็นประเพณีสำคัญของชาวนครศรีธรรมราชในปัจจุบัน

ระยะเวลาดำเนินการ
แต่เดิมการแห่ผ้าขึ้นธาตุนิยมจัดปีละสองครั้ง ในวันขึ้น ๑๕ ค่ำ เดือนสาม (วันมาฆบูชา) และวันขึ้น ๑๕ ค่ำ เดือนหก (วันวิสาขบูชา) โดยนำผ้าไปห่มองค์พระบรมธาตุเจดีย์ ณ วัดพระมหาธาตุวรมหาวิหาร ปัจจุบันนิยมทำกัน
ในวันขึ้น ๑๕ ค่ำ เดือนสาม (วันมาฆบูชา) มากกว่า

