๑. แสดงให้เห็นลักษณะสังคมของนครศรีธรรมราช ที่ยึดมั่นอยู่ในพระพุทธศาสนาการทำบุญเพื่ออุทิศเป็นพุทธบูชาเพื่อประสงค์ให้ใกล้ชิดกับพระพุทธเจ้า
๒. แสดงให้เห็นว่าองค์พระบรมธาตุเจดีย์ เป็นศูนย์รวมจิตใจ ศูนย์รวมความศรัทธา พุทธศาสนิกชนทั่วไปทุกทิศจึงประสงค์มาห่มผ้าพระธาตุอย่างพร้อมเพรียงกัน          </h5>
